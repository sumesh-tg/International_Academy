package com.zs.ina.admin.master.configpool.dao;

/**
 *
 * @author 100035
 */
public class NamePOJO {
    String fullName;
    String username;

    /**
     *
     */
    public NamePOJO() {
    }

    /**
     *
     * @param fullName
     * @param username
     */
    public NamePOJO(String fullName, String username) {
        this.fullName = fullName;
        this.username = username;
    }

    /**
     *
     * @return
     */
    public String getFullName() {
        return fullName;
    }

    /**
     *
     * @param fullName
     */
    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    /**
     *
     * @return
     */
    public String getUsername() {
        return username;
    }

    /**
     *
     * @param username
     */
    public void setUsername(String username) {
        this.username = username;
    }

    @Override
    public String toString() {
        return fullName;
    }
    
}
